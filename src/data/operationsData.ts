export interface RiskItem {
  title: string;
  description: string;
  severity: 'low' | 'medium' | 'high';
}

export interface ChannelRecommendation {
  name: string;
  purpose: string;
  priority: 'основной' | 'поддерживающий' | 'дополнительный';
}

export interface SegmentTactic {
  segmentId: 'analyst' | 'navigator' | 'unsystematic';
  focus: string;          // на что делать акцент
  tone: string;           // тон коммуникации
  doList: string[];       // что делать
  dontList: string[];     // что НЕ делать
}

export interface IncidentScenario {
  trigger: string;                 // что произошло
  immediateAction: string;         // что делать срочно
  responsible: TeamRole;           // кто отвечает
  supporting: TeamRole[];          // кто помогает
  howToDetect: string;             // как отследить (метрика / сигнал)
  preventionTip: string;           // как не допустить впредь
}

